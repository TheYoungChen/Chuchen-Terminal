function createProviderUsageMock(input: {
  providerProfileId: string
  appType: ProviderRequestLog['appType']
  model: string
  scale: number
  dataSource: string
}) {
  return {
    providerProfileId: input.providerProfileId,
    summary: {
      ...providerUsageSummaryMock,
      totalRequests: Math.round(providerUsageSummaryMock.totalRequests * input.scale),
      totalCostUsd: Number((providerUsageSummaryMock.totalCostUsd * input.scale).toFixed(4)),
      totalInputTokens: Math.round(providerUsageSummaryMock.totalInputTokens * input.scale),
      totalOutputTokens: Math.round(providerUsageSummaryMock.totalOutputTokens * input.scale),
      totalCacheReadTokens: Math.round(providerUsageSummaryMock.totalCacheReadTokens * input.scale),
      totalCacheCreationTokens: Math.round(providerUsageSummaryMock.totalCacheCreationTokens * input.scale),
    },
    trends: providerUsageTrendMock.map((point) => ({
      ...point,
      inputTokens: Math.round(point.inputTokens * input.scale),
      outputTokens: Math.round(point.outputTokens * input.scale),
      cacheReadTokens: Math.round(point.cacheReadTokens * input.scale),
      cacheCreationTokens: Math.round(point.cacheCreationTokens * input.scale),
      costUsd: Number((point.costUsd * input.scale).toFixed(4)),
    })),
    requestLogs: providerRequestLogMock.map((log, index) => ({
      ...log,
      id: `${input.providerProfileId}-req-${index + 1}`,
      providerProfileId: input.providerProfileId,
      appType: input.appType,
      model: input.model,
      inputTokens: Math.round(log.inputTokens * input.scale),
      outputTokens: Math.round(log.outputTokens * input.scale),
      cacheReadTokens: Math.round(log.cacheReadTokens * input.scale),
      cacheCreationTokens: Math.round(log.cacheCreationTokens * input.scale),
      costUsd: Number((log.costUsd * input.scale).toFixed(4)),
      dataSource: input.dataSource,
    })),
  }
}

