function resolvePreferredLocale(): AppLocale {
  if (typeof window === 'undefined') return 'zh-CN'

  const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (stored === 'zh-CN' || stored === 'en-US') return stored

  const navigatorLocale = window.navigator.language.toLocaleLowerCase()
  if (navigatorLocale.startsWith('zh')) return 'zh-CN'
  return 'en-US'
}

function applyDocumentLanguage(locale: AppLocale) {
  if (typeof document === 'undefined') return
  document.documentElement.lang = locale
}

export const i18n = createI18n({
  legacy: false,
  locale: resolvePreferredLocale(),
  fallbackLocale: 'zh-CN',
  messages,
  globalInjection: true,
  missingWarn: false,
  fallbackWarn: false,
})

applyDocumentLanguage(i18n.global.locale.value as AppLocale)

export function setAppLocale(locale: AppLocale) {
  i18n.global.locale.value = locale
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale)
  }
  applyDocumentLanguage(locale)
}
